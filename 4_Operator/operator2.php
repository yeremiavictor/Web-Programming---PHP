<?php
// operator penugasan

$data = 5;
echo "data" . $data;
echo "<br>";

// ini operator aritmatika
$data = $data + 10;
echo "data + 10 = $data";
echo "<br>";

// maka nilai data akan sama dengan 5 + 10 = 15

// ini operator penugasan
$data += 10;
echo "+= 10 = $data";

// sekarang nilai data akan menjadi 15 + 10 = 25


// Nama Operator dan simbol
// Pengisian Nilai	=
// Pengisian dan Penambahan	+=
// Pengisian dan Pengurangan	-=
// Pengisian dan Perkalian	*=
// Pengisian dan Pemangkatan	**=
// Pengisian dan Pembagian	/=
// Pengisian dan Sisa bagi	%=
// Pengisian dan Penggabungan (string)	.=